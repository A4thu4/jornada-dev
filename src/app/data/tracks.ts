export type ModuleStatus = 'bloqueado' | 'disponível' | 'em-progresso' | 'concluído';

export interface Module {
  id: string;
  title: string;
  description: string;
  icon: string;
  rating: number;
  lessonCount: number;
  status: ModuleStatus;
}

export interface Character {
  id: string;
  name: string;
  title: string;
  archetype: string;
  accentColor: string;
  accentGlow: string;
  icon: string;
  stats: { fogo: number; espada: number; escudo: number; xp: number };
  modules: Module[];
}

const makeModules = (prefix: string, titles: { title: string; description: string; icon: string }[]): Module[] =>
  titles.map((m, i) => ({
    id: `${prefix}-${i + 1}`,
    title: m.title,
    description: m.description,
    icon: m.icon,
    rating: Math.floor(Math.random() * 2) + 4,
    lessonCount: Math.floor(Math.random() * 8) + 6,
    status: i === 0 ? 'disponível' : 'bloqueado',
  }));

