export type ModuleStatus = 'bloqueado' | 'disponível' | 'em-progresso' | 'concluído';

export interface Module {
	id: string;
	title: string;
	description: string;
	icon: string;
	rating: number;
	lessonCount: number;
	status: ModuleStatus;
	link?: string;
}

export interface Character {
	id: string;
	name: string;
	title: string;
	archetype: string;
	accentColor: string;
	accentGlow: string;
	icon: string;
	stats: { nivel: number; progresso: number; missoes: number; xp: number };
	modules: Module[];
}

export interface SpecialPath extends Character {
	description: string;
	buttonLabel: string;
}

const makeModules = (prefix: string, titles: {
	title: string;
	description: string;
	icon: string;
	link?: string;
	rating: number;
	lessonCount?: number
}[]): Module[] =>
	titles.map((m, i) => ({
		id: `${prefix}-${i + 1}`,
		title: m.title,
		description: m.description,
		icon: m.icon,
		link: m.link,
		rating: m.rating ? m.rating : Math.floor(Math.random() * 2) + 2,
		lessonCount: m.lessonCount ? m.lessonCount : Math.floor(Math.random() * 8) + 6,
		status: i === 0 ? 'disponível' : 'bloqueado',
	}));

